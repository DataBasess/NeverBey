package com.epro.leave.common;

public class DropdownResponse {
	private Integer dropdownId;
	private String dropdownIdChar;
	private String dropdownValue1;
	private String dropdownValue2;
	
	public Integer getDropdownId() {
		return dropdownId;
	}
	public void setDropdownId(Integer dropdownId) {
		this.dropdownId = dropdownId;
	}
	public String getDropdownIdChar() {
		return dropdownIdChar;
	}
	public void setDropdownIdChar(String dropdownIdChar) {
		this.dropdownIdChar = dropdownIdChar;
	}
	public String getDropdownValue1() {
		return dropdownValue1;
	}
	public void setDropdownValue1(String dropdownValue1) {
		this.dropdownValue1 = dropdownValue1;
	}
	public String getDropdownValue2() {
		return dropdownValue2;
	}
	public void setDropdownValue2(String dropdownValue2) {
		this.dropdownValue2 = dropdownValue2;
	}
}
